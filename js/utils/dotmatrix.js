/**
 * Renderiza una cadena de texto en un contenedor usando elementos HTML simulando una matriz de puntos 5x7.
 * 
 * @param {HTMLElement} container Contenedor donde se montará la matriz
 * @param {string} text Texto a renderizar (soporta 0-9, :, espacio y A, P, M en mayúsculas)
 */
export function renderDotMatrix(container, text) {
  // Asegurar que el contenedor tenga la clase de matriz
  if (!container.classList.contains('dot-matrix-display')) {
    container.classList.add('dot-matrix-display');
  }

  // Limpiar contenedor
  container.innerHTML = '';

  const chars = text.toUpperCase().split('');
  
  chars.forEach(char => {
    const charContainer = document.createElement('div');
    charContainer.className = 'dot-matrix-char';

    // Obtener el patrón del caracter o espacio por defecto
    const pattern = FONT_5X7[char] || FONT_5X7[' '];

    // Recorrer filas (7) y columnas (5) para generar físicamente la rejilla de puntos
    for (let r = 0; r < 7; r++) {
      for (let c = 0; c < 5; c++) {
        const dot = document.createElement('div');
        dot.className = 'dot-matrix-dot';
        
        // Si el punto está encendido en el patrón, añadir clase activa
        if (pattern[r][c] === 1) {
          dot.classList.add('active');
        }
        
        charContainer.appendChild(dot);
      }
    }

    container.appendChild(charContainer);
  });
}
